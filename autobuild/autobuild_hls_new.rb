SYNTH_REPORT = FUNC_DIR + '\hls\impl\verilog\project.runs\bd_0_hls_inst_0_synth_1' +
               '\bd_0_hls_inst_0_utilization_synth.rpt'
EXP_REPORT   = FUNC_DIR + '\hls\impl\report\verilog\export_syn.rpt'
IP_FILE      = FUNC_DIR + '\hls\impl\ip' + "\\" + IP_VLNV.gsub(/[\.:]/, '_') + '.zip'
