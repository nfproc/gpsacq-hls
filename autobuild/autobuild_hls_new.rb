newpath = VITIS_DIR + '\bin;' + ENV['PATH']
newpath = HLS_DIR + '\bin;' + newpath       if VITIS_DIR != HLS_DIR
newenv = {'PATH' => newpath, 'XILINX_VITIS' => HLS_DIR, 'XILINX_HLS' => HLS_DIR}

########################################################################
# options to be evaluated
PAR_PRN = [1, 2, 4, 8, 16, 32]
PAR_FREQ = [1, 5, 25]
PAR_CODE = [1, 2, 3, 6, 11, 22, 31, 33, 62, 66, 93, 186, 341, 682, 1023]
