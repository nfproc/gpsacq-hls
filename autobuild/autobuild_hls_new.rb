def run_hls (hls_arg, hls_logs, newenv, prn, freq, code)
  args = [newenv]
  args << VITIS_DIR + '\bin\vitis-run.bat'   if hls_arg != '--csynth'
  args += [VITIS_DIR + '\bin\v++.bat', '-c'] if hls_arg == '--csynth'
  args << VITIS_DIR + '\bin\v++.bat'         if hls_arg == '--csynth'
  args += ['--mode', 'hls',
           '--work_dir', COMP_DIR,
           '--config', COMP_DIR + '\hls_config.cfg']
  args << hls_arg                            if hls_arg != '--csynth'

  IO.popen(args) do |io|
    while line = io.gets
      hls_logs << line
      if line =~ /Finished/
        puts "%2d|%2d|%4d|%s" % [prn, freq, code, line.chomp]
      end
    end
  end
  if ! $?.success?
    puts "!! failed to build the core (with status %d). stop." % $?
    exit 1
  end
end

########################################################################
# main loop

resource_over = []
PAR_PRN.product(PAR_FREQ, PAR_CODE).each do |prn, freq, code|
  next if resource_over.include?([prn, freq])
  puts "## build: PRN %d, FREQ %d, CODE %d" % [prn, freq, code]
  puts
  sleep 1
  
  # make HEADER_FILE
  open(HEADER_FILE, 'w') do |file|
    file.puts "// NOTE: this file is generated by autobuild.rb"
    file.puts "// degree of parallelism"
    file.puts "// PAR_PRN  should be a power of 2, up to 32 (1, 2, 4, 8, 16, 32)"
    file.puts "// PAR_FREQ should be a power of 5, up to 25 (1, 5, 25)"
    file.puts "// PAR_CODE should be a divisor of 2046 (1, 2, 3, 6, 11, 22, 31, 33, ...)"
    file.puts
    file.puts "#define PAR_PRN  %d" % prn
    file.puts "#define PAR_FREQ %d" % freq
    file.puts "#define PAR_CODE %d" % code 
  end

  # run Vitis HLS
  hls_logs = ""
  # run_hls('--csim', hls_logs, newenv, prn, freq, code)
  run_hls('--csynth', hls_logs, newenv, prn, freq, code) # --csynth is pseudo-command for calling v++
  run_hls('--impl', hls_logs, newenv, prn, freq, code)


  # copy relavant file
  logdir = 'log\prn%d_freq%d_code%d' % [prn, freq, code]
  FileUtils.rm_rf logdir
  FileUtils.mkdir logdir
  open(logdir + '\hls_log.txt', 'w'){|x| x.write(hls_logs) }
  # FileUtils.cp   CSIM_LOG    , logdir + '\csim_log.txt'
  FileUtils.cp_r REPORT_DIR  , logdir
  FileUtils.cp   SYNTH_REPORT, logdir + '\synth_rep.txt'
  FileUtils.cp   EXP_REPORT  , logdir + '\export_rep.txt'
  FileUtils.cp   IP_FILE     , logdir + '\export.zip'

  # detect resource over
  lut = ff = 0
  open(logdir + '/synth_rep.txt', 'r') do |file|
    while line = file.gets
      if line =~ /Slice LUTs[\* \|]+([0-9]+)/
        lut = $1.to_i
      elsif line =~ /Slice Registers[ \|]+([0-9]+)/
        ff = $1.to_i
      end
    end
  end
  puts "%2d|%2d|%4d| ## LUT = %d, FF = %d" % [prn, freq, code, lut, ff]
  resource_over << [prn, freq] if lut >= 50000 || ff >= 100000
end

